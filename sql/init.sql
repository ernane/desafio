-- Table: public.vendedor

-- DROP TABLE public.vendedor;

CREATE TABLE public.vendedor
(
    vendedor_id integer NOT NULL DEFAULT nextval('vendedor_id_seq'::regclass),
    vendedor_nome character varying(100) COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT vendedor_pkey PRIMARY KEY (vendedor_id)
