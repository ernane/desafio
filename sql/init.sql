    IS 'Nome do Vendedor';

-- Table: public.venda

-- DROP TABLE public.venda;

CREATE TABLE public.venda
(
    venda_id integer NOT NULL DEFAULT nextval('venda_id_seq'::regclass),
    vendedor_id integer,
    venda_data date,
    venda_valor real,
    CONSTRAINT venda_pkey PRIMARY KEY (venda_id),
    CONSTRAINT venda_vendedor_id_fkey FOREIGN KEY (vendedor_id)
        REFERENCES public.vendedor (vendedor_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT maior_que_zero CHECK (venda_valor > 0::double precision) NOT VALID
)
WITH (
    OIDS = FALSE
)
TABLESPACE pg_default;

ALTER TABLE public.venda
    OWNER to postgres;

COMMENT ON COLUMN public.venda.venda_id
    IS 'Identificador unico da venda.(PK)';

COMMENT ON COLUMN public.venda.vendedor_id
    IS 'Identificador do Vendedor.(FK)';

COMMENT ON COLUMN public.venda.venda_data
    IS 'Data da Realizacao da Venda';

COMMENT ON COLUMN public.venda.venda_valor
    IS 'Valor em R$ da Venda';


